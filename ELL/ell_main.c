void EllHex2(const char *fun_name, void * buff, int size)
{
    char str[200] = {0}, *ptr, *ptr1;
    int i = 0, j;
    ptr = (char*)str;
    ptr1 = (char*)buff;

    if(size <= 0)
    {
        return;
    }
    EllLog2(NULL, "HEX:%s()", fun_name);
    while(i < size)
    {
        sprintf(ptr, "%0.2X ", *ptr1);
        ptr += 3;
        if((i + 1) % 40 == 0)
        {
            EllLog2(NULL, "%s", str);
            memset(str, 0, sizeof(str));
            ptr = (char*)str;
        }
        ptr1++;
        i++;
    }
    if(strlen(str) > 0)
    {
        EllLog2(NULL, "%s", str);
    }
}

